let x = 1234;

console.log(x.toString()); // 문자열로 변환.

console.log((4567).toString()); // 문자열로 변환.

console.log((123.456).toFixed(1)); // 소수점 이하 자릿수를 몇 번째까지 반환할 것인지 지정. 반올림.

console.log(123.456.toPrecision(3)); // 정수, 소수점 자릿수를 몇 번째까지 반환할 것인지 지정. 반올림.

console.log(parseInt("1234.1234")); // 문자열을 정수로 변환.

console.log(parseFloat("1234.1234")); // 문자열을 실수 ( 부동소수점 javascript특징 ) 으로 변환.

